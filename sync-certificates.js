  'JavaScript.webp': { issuer: 'Sololearn', title: 'Introduction to JavaScript', featured: true },
  'css.webp': { issuer: 'Sololearn', title: 'Introduction to CSS', featured: false },
  'ab.webp': { issuer: 'Sololearn', title: 'A/B Testing Using AI', featured: false },
  'ai1_page-0001.webp': { issuer: 'Ethio Coders Initiative (Udemy)', title: 'Artificial Intelligence Fundamentals', featured: true },
  'learn.udacity.com_view-certificate_nd002-gc-251.webp': { issuer: 'Ethio Coders Initiative (Udemy)', title: 'Data Analysis Fundamentals', featured: true },
